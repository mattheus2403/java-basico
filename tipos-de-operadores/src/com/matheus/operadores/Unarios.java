package com.matheus.operadores;

public class Unarios {
	
	public static void main (String args[]) {
		
		//valores unarios definem se um numero
		//poderá ser negativo ou positivo
		
		int num = 5;
		
		//torna a variável negativa
		num = - num;
		
		System.out.println(num);
		
		//torna a variável positiva
		num = num * -1;
		
		System.out.println(num);
		
		
	}

}
